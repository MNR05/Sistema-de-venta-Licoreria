			select @idventa,IDPRODUCTO,PRECIOVENTA,CANTIDAD,@MontoTotal from @DetalleVenta	
			
		commit transaction registro
	end try
	begin catch
		set @Resultado=0
		set @Mensaje= ERROR_MESSAGE()

		rollback transaction registro
	end catch
end
